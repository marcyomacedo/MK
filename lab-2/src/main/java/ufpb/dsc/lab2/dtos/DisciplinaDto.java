package ufpb.dsc.lab2.dtos;

public class DisciplinaDto {
	
	private Long id;
	private String nome;
	
	public DisciplinaDto(Long id, String nome) {
		super();
		this.id = id;
		this.nome = nome;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}
	
	
	

}
